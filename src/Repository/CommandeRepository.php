     */
    public function findByUser(User $user): array
    {
        return $this->createQueryBuilder('c')
            ->leftJoin('c.menu', 'm')
            ->addSelect('m')
            ->andWhere('c.user = :user')
            ->setParameter('user', $user)
            ->orderBy('c.date_commande', 'DESC')
            ->getQuery()
            ->getResult();
    }
